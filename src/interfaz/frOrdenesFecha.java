    public static Date ajustarTiempoDesde(Date date) {
        Calendar cal = Calendar.getInstance();
        cal.setTime(date);
        cal.set(Calendar.HOUR_OF_DAY, 0);
        cal.set(Calendar.MINUTE, 0);
        cal.set(Calendar.SECOND, 0);
        cal.set(Calendar.MILLISECOND, 0);
        return cal.getTime();
    }

    private void limpiarTabla() {

        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);
    }

    public static Date ajustarTiempoHasta(Date date) {
        Calendar cal = Calendar.getInstance();
        cal.setTime(date);
        cal.set(Calendar.HOUR_OF_DAY, 23);
        cal.set(Calendar.MINUTE, 59);
        cal.set(Calendar.SECOND, 0);
        cal.set(Calendar.MILLISECOND, 0);
        return cal.getTime();
    }

    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        limpiarTabla();
        Date desde = jDateChooser1.getDate();
        Date hasta = jDateChooser2.getDate();

        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy hh:mm aa");

        desde = ajustarTiempoDesde(desde);
        hasta = ajustarTiempoHasta(hasta);

        ArrayList<Orden> ordenes = main.ProgramaPrincipal.datos.buscarOrdenesFecha(desde, hasta);
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

        String fecha;
        if (ordenes.size() > 0) {
            for (Orden o : ordenes) {
                fecha = formato.format(o.getFecha());
                modelo.addRow(new Object[]{o.getNumeroOrden(), fecha, o.getCliente().getNombre(), o.getTotal()});
            }

            totalOrden();
        } else {
            limpiarTabla();
        }
    }//GEN-LAST:event_jButtonBuscarActionPerformed

