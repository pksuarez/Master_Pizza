    private void jButtonImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonImprimirActionPerformed

        if (!alternativo) {

            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            List<Orden> ordenes = new ArrayList<>();
            Orden o;
            int numero;
            for (int i = 0; i < modelo.getRowCount(); i++) {
                numero = Integer.parseInt(String.valueOf(modelo.getValueAt(i, 0)));
                o = main.ProgramaPrincipal.datos.obtenerOrden(numero);
                ordenes.add(o);
            }

            Factura f = new Factura();
            f.imprimirVentas(ordenes, jLabeltotal.getText(), main.ProgramaPrincipal.config.getImpresora());
        } else {
            int numero = Integer.parseInt(String.valueOf(jTable1.getValueAt(jTable1.getSelectedRow(), 0)));
            Orden o = main.ProgramaPrincipal.datos.obtenerOrden(numero);

            Factura f = new Factura();
            f.reImprimirFactura(o, main.ProgramaPrincipal.config.getImpresora());

        }
    }//GEN-LAST:event_jButtonImprimirActionPerformed

    private void totalOrden() {

        long total = 0;
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            String precio = String.valueOf(jTable1.getValueAt(i, 3));
            total += Long.valueOf(precio);
        }
        String s = String.format("%,d", total);
        jLabeltotal.setText(s);
    }

