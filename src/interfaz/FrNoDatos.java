    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        JFileChooser archivo = new JFileChooser();
        archivo.setDialogTitle("Carpeta de donde cargar");
        archivo.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int retorno = archivo.showOpenDialog(this);
        if (retorno == JFileChooser.APPROVE_OPTION) {
            String root = archivo.getSelectedFile().toString();

            String pathCli = root + "\\" + "Clientes";
            String pathEmp = root + "\\" + "Empleados";
            String pathOrd = root + "\\" + "Ordenes";
            String pathPiz = root + "\\" + "Pizzas";
            String pathBeb = root + "\\" + "Bebidas";
            String pathOtr = root + "\\" + "Productos";
            String pathTop = root + "\\" + "Toppigns";
            String pathCaj = root + "\\" + "Cajas";

            datos.cargarPizzas(pathPiz);
            datos.cargarBebidas(pathBeb);
            datos.cargarToppings(pathTop);
            datos.cargarOtros(pathOtr);
            datos.cargarClientes(pathCli);
            datos.cargarEmpleados(pathEmp);
            datos.cargarOdenes(pathOrd);
            datos.cargarCajas(pathCaj);

            File directory = new File("datos");
            if (!directory.exists()) {
                directory.mkdir();
            }

            datos.guardarBebidas(main.ProgramaPrincipal.pathB);
            datos.guardarCajas(main.ProgramaPrincipal.pathCajas);
            datos.guardarClientes(main.ProgramaPrincipal.pathC);
            datos.guardarEmpleados(main.ProgramaPrincipal.pathE);
            datos.guardarOrdenes(main.ProgramaPrincipal.pathO);
            datos.guardarOtros(main.ProgramaPrincipal.pathProd);
            datos.guardarPizzas(main.ProgramaPrincipal.pathP);
            datos.guardarToppings(main.ProgramaPrincipal.pathT);

            FrPrincipal fr = new FrPrincipal();
            fr.setVisible(true);
            this.dispose();

        }
    }//GEN-LAST:event_jButton2ActionPerformed

