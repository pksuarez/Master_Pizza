    private ArrayList<Cliente> buscarPorTelefono() {
        Lista clientes = (Lista) ProgramaPrincipal.datos.getClientes();
        ArrayList<Cliente> encontrados = new ArrayList<>();
        int largo = clientes.largo();
        String busqueda = jTextFieldBusqueda.getText();
        Cliente c = null;

        for (int i = 0; i < largo; i++) {
            c = (Cliente) clientes.obtener(i);
            if (c.getTelefono().contains(busqueda)) {
                encontrados.add(c);
            }
        }
        return encontrados;
    }

    private ArrayList<Cliente> buscarPorNombre() {
        Lista clientes = (Lista) ProgramaPrincipal.datos.getClientes();
        ArrayList<Cliente> encontrados = new ArrayList<>();
        int largo = clientes.largo();
        String busqueda = jTextFieldBusqueda.getText().toLowerCase();
        Cliente c = null;
        for (int i = 0; i < largo; i++) {
            c = (Cliente) clientes.obtener(i);
            if (c.getNombre().toLowerCase().contains(busqueda)) {
                encontrados.add(c);
            }
        }
        return encontrados;
    }

    private ArrayList<Cliente> buscarPorApellido() {
        Lista clientes = (Lista) ProgramaPrincipal.datos.getClientes();
        ArrayList<Cliente> encontrados = new ArrayList<>();
        int largo = clientes.largo();
        String busqueda = jTextFieldBusqueda.getText().toLowerCase();
        Cliente c = null;
        for (int i = 0; i < largo; i++) {
            c = (Cliente) clientes.obtener(i);
            if (c.getApellido().toLowerCase().contains(busqueda)) {
                encontrados.add(c);
            }
        }
        return encontrados;
    }

    private void llenarTabla(ArrayList<Cliente> encontrados) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int cant = model.getRowCount();
        model.setRowCount(0);
        
        String tel;
        for (Cliente c : encontrados) {
            tel = formatear(c.getTelefono());
            model.addRow(new Object[]{c.getNombre(), c.getApellido(),tel});
        }

    }

