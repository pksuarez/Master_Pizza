    private void JButtonSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JButtonSeleccionarActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int row = jTable1.getSelectedRow();
        Cliente cliente = ProgramaPrincipal.datos.obtenerCliente(desformatear(String.valueOf(model.getValueAt(row, 2))));
        int opcion = JOptionPane.showConfirmDialog(this, "Seguro que desea eliminar este cliente?","Eliminar",JOptionPane.YES_NO_OPTION);
        
        if (opcion == JOptionPane.YES_OPTION){
            ProgramaPrincipal.datos.getClientes().eliminar(cliente);
            ProgramaPrincipal.datos.guardarClientes(ProgramaPrincipal.pathC);
            model.removeRow(jTable1.getSelectedRow());
        }
        
    }//GEN-LAST:event_JButtonSeleccionarActionPerformed

