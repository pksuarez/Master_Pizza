    private void llenarTabla() {

        jTable1.getTableHeader().setFont(new Font("Tahoma", Font.PLAIN, 24));
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int cant = ProgramaPrincipal.datos.getClientes().largo();

        if (cant > 0) {
            String tel;
            for (int i = 0; i < cant; i++) {
                Cliente c = (Cliente) ProgramaPrincipal.datos.getClientes().obtener(i);
                tel = formatear(c.getTelefono());
                model.addRow(new Object[]{c.getNombre() , c.getApellido(), tel});
            }
        }

    }
    
    private void buscar(){
        boolean tel = jRadioTel.isSelected();
        boolean nombre = JRadioNombre.isSelected();
        boolean apellido = jRadioApellido.isSelected();

        ArrayList<Cliente> encontrados = new ArrayList<>();

        if (tel) {
            encontrados = buscarPorTelefono();
        }

        if (nombre) {
            encontrados = buscarPorNombre();
        }

        if (apellido) {
            encontrados = buscarPorApellido();
        }

        llenarTabla(encontrados);
    }
