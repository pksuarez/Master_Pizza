    private void llenarCombobox() {

        Lista empleados = main.ProgramaPrincipal.datos.getEmpleados();

        for (int i = 0; i < empleados.cantidad(); i++) {
            Empleado e = (Empleado) empleados.obtener(i);
            jComboBox1.addItem(e.getLogin());
        }
    }

