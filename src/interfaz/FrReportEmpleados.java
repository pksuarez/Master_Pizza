    public static Date ajustarTiempoDesde(Date date) {
        Calendar cal = Calendar.getInstance();
        cal.setTime(date);
        cal.set(Calendar.HOUR_OF_DAY, 0);
        cal.set(Calendar.MINUTE, 0);
        cal.set(Calendar.SECOND, 0);
        cal.set(Calendar.MILLISECOND, 0);
        return cal.getTime();
    }

    public static Date ajustarTiempoHasta(Date date) {
        Calendar cal = Calendar.getInstance();
        cal.setTime(date);
        cal.set(Calendar.HOUR_OF_DAY, 23);
        cal.set(Calendar.MINUTE, 59);
        cal.set(Calendar.SECOND, 0);
        cal.set(Calendar.MILLISECOND, 0);
        return cal.getTime();
    }

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String login = String.valueOf(jComboBox1.getSelectedItem());
        Empleado e = main.ProgramaPrincipal.datos.obtenerEmpleado(login, login);
        Date desde = jDateChooser1.getDate();
        Date hasta = jDateChooser2.getDate();
        desde = ajustarTiempoDesde(desde);
        hasta = ajustarTiempoHasta(hasta);
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy hh:mm aa");

        ArrayList<ClockIn> fechas = e.obtenerFechas(desde, hasta);

        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

        if (fechas.size() > 0) {
            limpiarTabla();
            for (ClockIn clock : fechas) {

                Date d1 = clock.getEntrada();
                Date d2 = clock.getSalida();

                String f1 = formato.format(d1);
                String f2 = formato.format(d2);

                modelo.addRow(new Object[]{f1, f2});
            }
        } else {
            limpiarTabla();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void limpiarTabla() {

        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);
    }

