    private String formatear(String num) {
        String s1 = num.substring(0, 4) + "-";
        s1 += num.substring(4);
        return s1;
    }

    private String desformatear(String num) {
        String[] formateado = num.split("-");
        String desformateado = "";
        for (int i = 0; i < formateado.length; i++) {
            desformateado += formateado[i];
        }
        return desformateado;
    }
    
    private void llenarInfo(String numero, int opcion) {
        jButtonSalir.setVisible(false);
        Cliente c = ProgramaPrincipal.datos.obtenerCliente(numero);
        jTextAreaDir.setText(c.getDireccion());
        jTextFieldNombre.setText(c.getNombre());
        jTextFieldApellido.setText(c.getApellido());
        jTextFieldTel.setText(formatear(numero));
        if (opcion == 0) {
            jTextAreaDir.setEditable(false);
            jTextFieldApellido.setEditable(false);
            jTextFieldNombre.setEditable(false);
            jTextFieldTel.setEditable(false);
            jButton1.setVisible(false);
            jButton2.setVisible(false);
            jButtonSalir.setVisible(true);

        }

    }

