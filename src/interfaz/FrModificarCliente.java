    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jButton1.getText().equals("Salir")) {
            this.dispose();
            FrMostrarClientes fr = new FrMostrarClientes();
            fr.setVisible(true);
        } else if (jButton1.getText().equals("Modificar")) {
            Cliente c = ProgramaPrincipal.datos.obtenerCliente(numero);
            if (jTextFieldTel.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "No puede dejar el telefono en blanco!");
            } else {
                Cliente check = ProgramaPrincipal.datos.obtenerCliente(jTextFieldTel.getText());

                if (numero.equals(desformatear(jTextFieldTel.getText()))) {
                    c.setApellido(jTextFieldApellido.getText());
                    c.setNombre(jTextFieldNombre.getText());
                    c.setTelefono(numero);
                    c.setDireccion(jTextAreaDir.getText());
                    JOptionPane.showMessageDialog(this, "Modificado con exito!");
                    ProgramaPrincipal.datos.getClientes().ordenar();
                    ProgramaPrincipal.datos.guardarClientes(ProgramaPrincipal.pathC);
                    this.dispose();
                    FrMostrarClientes fr = new FrMostrarClientes();
                    fr.setVisible(true);
                } else {
                    if (check == null) {
                        c.setApellido(jTextFieldApellido.getText());
                        c.setNombre(jTextFieldNombre.getText());
                        c.setTelefono(desformatear(jTextFieldTel.getText()));
                        c.setDireccion(jTextAreaDir.getText());
                        JOptionPane.showMessageDialog(this, "Modificado con exito!");
                        ProgramaPrincipal.datos.guardarClientes(ProgramaPrincipal.pathC);
                        this.dispose();
                        FrMostrarClientes fr = new FrMostrarClientes();
                        fr.setVisible(true);
                    } else {
                        JOptionPane.showMessageDialog(this, "Ya existe un cliente con ese telefono! Intente con uno diferente.");
                    }
                }
            }
        }


    }//GEN-LAST:event_jButton1ActionPerformed

