    private String desformatear(String num) {
        String[] formateado = num.split("-");
        String desformateado = "";
        for (int i = 0; i < formateado.length; i++) {
            desformateado += formateado[i];
        }
        return desformateado;
    }
    
    private void buscar(){
        limpiarTabla();
        boolean extensa = jRadioButtonNombre.isSelected();
        String buscado = desformatear(jTextFieldCriterio.getText());
        ArrayList<Orden> encontradas = main.ProgramaPrincipal.datos.buscarOrden(criterio, buscado, extensa);
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy hh:mm aa");
        String fecha;
        if (encontradas.size() > 0) {
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

            for (Orden o : encontradas) {
                fecha = formato.format(o.getFecha());
                modelo.addRow(new Object[]{o.getNumeroOrden(), fecha, o.getCliente().getNombre(), o.getEmpleado().getNombre(), o.getTotal()});
            }
        }

        totalOrden();
        formatearNumeros();
    }
    private void jTextFieldCriterioKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldCriterioKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            buscar();
        }
    }//GEN-LAST:event_jTextFieldCriterioKeyPressed

